#include<bits/stdc++.h>
using namespace std;

int main(){
    int i;
    int io=0;
    int ii=0;
//for loop
    for(i = 0 ; i<=4 ; i++)
    {
        cout << "Dhaval " << i << endl;
    }
//while loop
    while(io<=4){
         
        cout << "Dhaval " << io << endl;
        io++;
    }
//do while loop
    do{
        cout << "Dhaval " << ii << endl;
        ii++;
    }while(ii<=4);

    return 0;
}